def is_congruence(cls):
    for a, a2 in product(VALUES, repeat=2):
        if cls[a] != cls[a2]:
            continue
        if cls[NOT(a)] != cls[NOT(a2)]:
            return False
        for b, b2 in product(VALUES, repeat=2):
            if cls[b] != cls[b2]:
                continue
            for op in OPS2.values():
                if cls[op(a, b)] != cls[op(a2, b2)]:
                    return False
    return True


# ------------------------------------------------------ 3. clone closure
def close_clone(arity):
    """All term operations of the given arity, by closure from
    projections under the fundamental operations."""
    points = list(product(VALUES, repeat=arity))
    projs = [tuple(pt[i] for pt in points) for i in range(arity)]
    clone = set(projs)
    frontier = list(clone)
    while frontier:
        new = set()
        current = list(clone)
        for f in frontier:
            g = tuple(NOT(x) for x in f)
            if g not in clone:
                new.add(g)
        for f in frontier:
            for g in current:
                for op in OPS2.values():
                    for h in (tuple(op(a, b) for a, b in zip(f, g)),
                              tuple(op(b, a) for a, b in zip(f, g))):
                        if h not in clone:
                            new.add(h)
        clone |= new
        frontier = list(new)
    return clone, projs, points


def external(fn):
    return all(v in (T, F) for v in fn)


