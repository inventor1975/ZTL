# -*- coding: utf-8 -*-
"""
Экспедиция Э6: вещественные и несчётность через незаработанное тождество.

Объекты двух презентаций:
  * ПОТОК — вещественное-в-становлении: функция i ↦ цифра; на времени t
    проверен префикс длины t. Равенство потоков — атом:
    префиксы разошлись → F (апартность заработана конечным свидетелем);
    совпадают до t → Z (не заработано). T НЕ ЗАРАБАТЫВАЕТСЯ НИКОГДА.
  * ПРЕДЪЯВЛЕНИЕ — конечно-предъявленный объект (дробь p/q): равенство
    решается конечно → атом T/F, классика.

Членство/реестры — те же ∃-свёртки, что в zsets.py (таблицы ядра).

Гипотеза куратора: «несчётность ℝ уложится в Z». Промеры покажут точную
форму ответа: Z ловит НЕРЕГИСТРИРУЕМОСТЬ (свойство презентации), а
диагональ Кантора добавляет отдельный МОЩНОСТНОЙ провал — и в классике
эти две разные невозможности слиты, а здесь расщепляются.
"""

from fractions import Fraction

from ztl import T, F, Z, OPS2, NOT

OR, AND = OPS2["or"], OPS2["and"]


# ------------------------------------------------------------- потоки
def stream_of_fraction(fr, base=10):
    """Поток десятичных цифр дроби (детерминированный, но предъявлен
    ТОЛЬКО экстенсионально — цифра за цифрой)."""
    def digit(i):
        return int(fr * base ** (i + 1)) % base
    return digit


def eq_stream_at(x, y, t):
    """Атом равенства потоков на времени t: F/Z (T не зарабатывается)."""
    for i in range(t):
        if x(i) != y(i):
            return F          # апартность заработана свидетелем i
    return Z                  # совпадение до t — тождество не заработано


def mem_stream_at(x, registry, t):
    """x ∈ реестр (вердикт на времени t): ∃-свёртка атомов таблицами."""
    v = F
    for y in registry:
        v = OR(v, eq_stream_at(x, y, t))
    return v


def apart_time(x, y, t_max=200):
    """Время, за которое зарабатывается апартность (или None)."""
    for i in range(t_max):
        if x(i) != y(i):
            return i + 1
    return None


def diagonal(registry, base=10):
    """Диагональ Кантора: против i-го пункта переворачиваем i-ю цифру."""
    def digit(i):
        if i < len(registry):
            return (registry[i](i) + 1) % base
        return 0
    return digit


# --------------------------------------------- конечные предъявления (ℚ)
def eq_pres(a, b):
    """Равенство предъявлений p/q — классический атом (решается конечно)."""
    return T if a == b else F


def mem_pres(x, registry):
    v = F
    for y in registry:
        v = OR(v, eq_pres(x, y))
    return v


# ---------------------------------------------------------------- промеры
if __name__ == "__main__":
    print("=" * 72)
    print("Э6. ВЕЩЕСТВЕННЫЕ: НЕЗАРАБОТАННОЕ ТОЖДЕСТВО И ДВА ПРОВАЛА ПЕРЕСЧЁТА")
    print("=" * 72)

    fr = [Fraction(1, 3), Fraction(1, 7), Fraction(22, 7) % 1,
          Fraction(1, 3)]                      # последний = дубль первого!
    R = [stream_of_fraction(f) for f in fr]    # реестр из четырёх потоков

    print("\n### Судьба атома равенства потоков (t = 5, 20, 100)")
    pairs = [("R0 против R1 (1/3 vs 1/7)", R[0], R[1]),
             ("R0 против R3 (1/3 vs 1/3 — ДУБЛЬ)", R[0], R[3]),
             ("R0 против самого себя", R[0], R[0])]
    for name, a, b in pairs:
        vals = [eq_stream_at(a, b, t) for t in (5, 20, 100)]
        print(f"  {name:42s} {' '.join(vals)}")
    print("  Разные — F (апартность заработана); совпадающие — вечное Z:")
    print("  T не зарабатывается НИ ПРИ КАКОМ t — сверка бесконечна.")

    print("\n### Провал №1 (зеротрастовый): реестр не сертифицирует НИКОГО")
    for t in (5, 20, 100):
        verdicts = [mem_stream_at(x, R, t) for x in R]
        print(f"  t={t:3d}: членство каждого из R в самом R: "
              f"{' '.join(verdicts)}")
    print("  Даже потоки, буквально стоящие в списке (включая дубль!), не")
    print("  зарабатывают членства: ∃-свёртка по {F, Z} даёт F. Нумерация")
    print("  потоков не может ПОДТВЕРДИТЬ покрытие ни одного элемента.")

    print("\n### Провал №2 (канторовский): диагональ зарабатывает апартность")
    d = diagonal(R)
    for i, y in enumerate(R):
        ta = apart_time(d, y)
        print(f"  диагональ против R{i}: апартность заработана за t={ta}")
    print(f"  членство диагонали в R (t=100): {mem_stream_at(d, R, 100)}")
    print("  Диагональ не просто вне списка — её невхождение ЗАРАБОТАНО")
    print("  конечными свидетелями против каждого пункта.")

    print("\n### Контраст: конечно-предъявленные (ℚ как дроби) регистрируются")
    Q = [Fraction(1, 3), Fraction(1, 7), Fraction(22, 7) % 1]
    for q in Q + [Fraction(2, 5)]:
        print(f"  {str(q):6s} ∈ реестру дробей: {mem_pres(q, Q)}")
    print("  Предъявление решает равенство конечно → атомы T/F → реестр")
    print("  сертифицирует каждый свой элемент. Счётность ℚ — это")
    print("  ЗАРАБОТИМОСТЬ ТОЖДЕСТВА ПРЕЗЕНТАЦИЙ, не факт о величине.")

    print("\n### Итог (форма ответа на гипотезу)")
    print("  Несчётность распалась на ДВЕ разные невозможности:")
    print("  1) НЕРЕГИСТРИРУЕМОСТЬ — зеротрастовая, про презентацию:")
    print("     экстенсиональные потоки не зарабатывают тождества, значит")
    print("     никакой реестр не сертифицирует ни строки (даже свои).")
    print("  2) НЕПОЛНОТА — канторовская, про мощность: диагональ")
    print("     зарабатывает конечных свидетелей против каждого пункта.")
    print("  Классика сливает их в одно слово «несчётно»; Z-оптика")
    print("  расщепляет — и №1 действительно «уложилась в Z» целиком.")
