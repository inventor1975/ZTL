def subalgebras():
    subs = []
    universe = list(VALUES)
    for mask in range(1, 8):
        S = [v for i, v in enumerate(universe) if mask >> i & 1]
        ok = all(NOT(a) in S for a in S) and \
            all(op(a, b) in S for op in OPS2.values() for a in S for b in S)
        if ok:
            subs.append(S)
    return subs


